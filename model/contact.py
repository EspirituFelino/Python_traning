class Contact:

    def __init__(self, first_name=None, middle_name=None, last_name=None, home=None, email=None, company=None, title=None, address=None):
        self.first_name = first_name
        self.last_name = last_name
        self.email = email
        self.home = home
        self.company = company
        self.title = title
        self.address = address
        self.middle_name = middle_name

